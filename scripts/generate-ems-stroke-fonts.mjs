// Generates the bundled EMS single-line font data from a pinned upstream commit.
// Run manually when intentionally refreshing the source fonts; normal builds use
// the checked-in generated TypeScript and never require network access.

import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const UPSTREAM_COMMIT = '8c71f2d9e1a5292047bb88e5595a766241b82cc6';
const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const OUTPUT = path.join(ROOT, 'src/core/text/ems-stroke-font-data.ts');

const FONTS = [
  { key: 'ems-allure', file: 'EMSAllure.svg', name: 'EMS Allure' },
  { key: 'ems-delight', file: 'EMSDelight.svg', name: 'EMS Delight' },
  { key: 'ems-tech', file: 'EMSTech.svg', name: 'EMS Tech' },
  { key: 'ems-osmotron', file: 'EMSOsmotron.svg', name: 'EMS Osmotron' },
];

const ATTR_PATTERN = /([\w-]+)="([^"]*)"/g;
const GLYPH_PATTERN = /<glyph\s+([^>]*?)\/>/g;
const SUPPORTED_PATH_COMMANDS = new Set(['M', 'L', 'C']);

function fail(message) {
  throw new Error(`generate-ems-stroke-fonts: ${message}`);
}

function attributes(source) {
  return Object.fromEntries(
    [...source.matchAll(ATTR_PATTERN)].map((match) => [match[1], match[2]]),
  );
}

function decodeXml(value) {
  return value
    .replaceAll('&quot;', '"')
    .replaceAll('&apos;', "'")
    .replaceAll('&lt;', '<')
    .replaceAll('&gt;', '>')
    .replaceAll('&amp;', '&')
    .replace(/&#x([0-9a-f]+);/gi, (_, digits) => String.fromCodePoint(Number.parseInt(digits, 16)))
    .replace(/&#([0-9]+);/g, (_, digits) => String.fromCodePoint(Number.parseInt(digits, 10)));
}

function requiredNumber(value, label) {
  const parsed = Number(value);
  if (!Number.isFinite(parsed)) fail(`invalid ${label}: ${String(value)}`);
  return parsed;
}

function parseFont(svg, expectedName) {
  const metadata = svg.match(/<metadata>([\s\S]*?)<\/metadata>/)?.[1] ?? '';
  if (!metadata.includes(`Font name:               ${expectedName}`)) {
    fail(`${expectedName} metadata name mismatch`);
  }
  if (!metadata.includes('License:                 SIL Open Font License')) {
    fail(`${expectedName} is not marked SIL Open Font License`);
  }
  const fontAttrs = attributes(svg.match(/<font\s+([^>]+)>/)?.[1] ?? '');
  const faceAttrs = attributes(svg.match(/<font-face\s+([\s\S]*?)\/>/)?.[1] ?? '');
  const defaultAdvance = requiredNumber(
    fontAttrs['horiz-adv-x'],
    `${expectedName} default advance`,
  );
  const capHeight = requiredNumber(faceAttrs['cap-height'], `${expectedName} cap height`);
  const glyphs = {};
  for (const match of svg.matchAll(GLYPH_PATTERN)) {
    const attrs = attributes(match[1] ?? '');
    if (attrs.unicode === undefined) continue;
    const character = decodeXml(attrs.unicode);
    if (Array.from(character).length !== 1) continue;
    const pathData = attrs.d;
    if (pathData !== undefined) validatePath(pathData, expectedName, character);
    glyphs[character] = {
      advance: requiredNumber(
        attrs['horiz-adv-x'] ?? defaultAdvance,
        `${expectedName} glyph advance`,
      ),
      ...(pathData === undefined ? {} : { path: pathData }),
    };
  }
  if (glyphs['?'] === undefined || glyphs[' '] === undefined) {
    fail(`${expectedName} must provide space and fallback glyphs`);
  }
  return { capHeight, defaultAdvance, glyphs };
}

function validatePath(pathData, fontName, character) {
  const commands = pathData.match(/[A-DF-Za-df-z]/g) ?? [];
  const unsupported = commands.find((command) => !SUPPORTED_PATH_COMMANDS.has(command));
  if (unsupported !== undefined) {
    fail(`${fontName} glyph ${JSON.stringify(character)} uses unsupported ${unsupported}`);
  }
}

async function loadFont(entry) {
  const url = `https://gitlab.com/oskay/svg-fonts/-/raw/${UPSTREAM_COMMIT}/fonts/EMS/${entry.file}`;
  const response = await fetch(url);
  if (!response.ok) fail(`${entry.file} fetch failed with ${response.status}`);
  const svg = await response.text();
  return {
    key: entry.key,
    displayName: entry.name,
    sourceFile: entry.file,
    sourceSha256: crypto.createHash('sha256').update(svg).digest('hex'),
    ...parseFont(svg, entry.name),
  };
}

const fonts = await Promise.all(FONTS.map(loadFont));
const generated = [
  '// Generated by scripts/generate-ems-stroke-fonts.mjs. Do not edit by hand.',
  `// Source: https://gitlab.com/oskay/svg-fonts/-/commit/${UPSTREAM_COMMIT}`,
  '// Each source SVG identifies the font as SIL Open Font License 1.1.',
  '',
  '// prettier-ignore -- generated vector data stays compact and out of file-size budgets.',
  `export const EMS_STROKE_FONT_DATA = ${JSON.stringify(fonts)} as const;`,
  '',
].join('\n');

fs.writeFileSync(OUTPUT, generated);
process.stdout.write(`wrote ${path.relative(ROOT, OUTPUT)} (${generated.length} chars)\n`);
