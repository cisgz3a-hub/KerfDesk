/**
 * T3-3: generate PROJECT_MAP.md from the filesystem.
 *
 * Usage:
 *   node scripts/generate-project-map.mjs
 *   node scripts/generate-project-map.mjs --check
 */
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const ROOT = join(SCRIPT_DIR, '..');
const PROJECT_MAP_PATH = join(ROOT, 'PROJECT_MAP.md');

const SCANNED_ROOTS = ['src', 'tests'];
const IGNORED_DIRS = new Set([
  '.git',
  'dist',
  'dist-electron',
  'node_modules',
  'release',
]);

function toPosix(path) {
  return path.replace(/\\/g, '/');
}

function walkFiles(rootName, dir = join(ROOT, rootName), out = []) {
  if (!existsSync(dir)) return out;

  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (!IGNORED_DIRS.has(entry.name)) {
        walkFiles(rootName, join(dir, entry.name), out);
      }
    } else if (entry.isFile()) {
      out.push(toPosix(relative(ROOT, join(dir, entry.name))));
    }
  }

  return out.sort();
}

function groupByBoundary(files, rootName) {
  const groups = new Map();
  for (const file of files) {
    const rest = file.slice(rootName.length + 1);
    const boundary = rest.includes('/') ? rest.slice(0, rest.indexOf('/')) : '(root)';
    const bucket = groups.get(boundary) ?? [];
    bucket.push(file);
    groups.set(boundary, bucket);
  }
  return [...groups.entries()].sort(([a], [b]) => a.localeCompare(b));
}

function renderBoundaryTable(title, rootName, files) {
  const rows = groupByBoundary(files, rootName).map(([boundary, group]) => {
    const childDirs = new Set();
    for (const file of group) {
      const parts = file.slice(rootName.length + 1).split('/');
      if (parts.length > 2) childDirs.add(parts[1]);
    }
    const children = [...childDirs].sort().join(', ') || '-';
    return `| \`${rootName}/${boundary === '(root)' ? '' : boundary}\` | ${group.length} | ${children} |`;
  });

  return [
    `## ${title}`,
    '',
    '| Boundary | Files | Direct child directories |',
    '|---|---:|---|',
    ...rows,
    '',
  ];
}

function renderFileList(title, rootName, files) {
  const lines = [`## ${title}`, ''];
  for (const [boundary, group] of groupByBoundary(files, rootName)) {
    const label = boundary === '(root)' ? rootName : `${rootName}/${boundary}`;
    lines.push(`### \`${label}\``, '');
    for (const file of group) {
      lines.push(`- \`${file}\``);
    }
    lines.push('');
  }
  return lines;
}

function renderProjectMap() {
  const srcFiles = walkFiles('src');
  const testFiles = walkFiles('tests');

  const lines = [
    '# LaserForge Project Map',
    '',
    '> AUTO-GENERATED by `npm run project-map:generate`.',
    '> Do not edit this file by hand. Run `npm run project-map:check` to verify it is current.',
    '',
    '## Summary',
    '',
    '| Area | Files | Source of truth |',
    '|---|---:|---|',
    `| \`src/\` | ${srcFiles.length} | Production code, UI, controller, app, geometry, import, storage, and worker modules |`,
    `| \`tests/\` | ${testFiles.length} | Executable regression, integration, source-pin, simulator, and E2E tests |`,
    '',
    '## Pipeline Overview',
    '',
    '```text',
    'Scene -> Job -> Plan -> Output -> Device',
    '```',
    '',
    'The source tree is the authority for detailed ownership. This generated map is a stable index for orientation and drift detection.',
    '',
    ...renderBoundaryTable('Source Module Boundaries', 'src', srcFiles),
    ...renderBoundaryTable('Test Boundaries', 'tests', testFiles),
    ...renderFileList('Source Files', 'src', srcFiles),
    ...renderFileList('Test Files', 'tests', testFiles),
  ];

  return `${lines.join('\n').replace(/\s+$/u, '')}\n`;
}

const expected = renderProjectMap();
const checkOnly = process.argv.includes('--check');

if (checkOnly) {
  const actual = existsSync(PROJECT_MAP_PATH)
    ? readFileSync(PROJECT_MAP_PATH, 'utf-8')
    : '';

  if (actual !== expected) {
    console.error('PROJECT_MAP.md is stale. Run `npm run project-map:generate`.');
    process.exit(1);
  }

  console.log('PROJECT_MAP.md is up to date.');
  process.exit(0);
}

writeFileSync(PROJECT_MAP_PATH, expected, 'utf-8');
console.log('Wrote PROJECT_MAP.md.');
